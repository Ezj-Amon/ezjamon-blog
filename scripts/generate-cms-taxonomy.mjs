import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import yaml from "js-yaml";

const root = process.cwd();
const outputPath = path.join(root, "src", "data", "cmsTaxonomy.ts");

const sources = [
  { key: "posts", dir: path.join(root, "src", "content", "posts") },
  { key: "resources", dir: path.join(root, "src", "content", "resources") },
];

const recommended = {
  posts: {
    categories: [
      "AI 搜索",
      "建站笔记",
      "前端开发",
      "博客建设",
      "视觉主题",
      "版本更新",
      "Amazon",
    ],
    subcategories: ["GEO", "AstroPaper", "发布流程", "示例项目", "配色"],
  },
  resources: {
    categories: ["游戏资料", "资源收藏", "AI 搜索", "前端开发", "建站笔记"],
    subcategories: ["文章", "视频", "课程", "工具", "书签", "代码仓库", "游戏"],
  },
};

async function listMarkdownFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async entry => {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) return listMarkdownFiles(fullPath);
      if (/\.(md|mdx)$/i.test(entry.name)) return [fullPath];
      return [];
    })
  );

  return files.flat();
}

function extractFrontmatter(content) {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) return {};

  const parsed = yaml.load(match[1]);
  return parsed && typeof parsed === "object" ? parsed : {};
}

function addCount(map, value) {
  if (typeof value !== "string") return;
  const normalized = value.trim();
  if (!normalized) return;
  map.set(normalized, (map.get(normalized) ?? 0) + 1);
}

function toOptions(map, fallbackValues = []) {
  fallbackValues.forEach(value => {
    if (!map.has(value)) map.set(value, 0);
  });

  return Array.from(map.entries())
    .map(([value, count]) => ({
      label: count > 0 ? `${value} (${count})` : value,
      value,
      count,
    }))
    .sort(
      (a, b) =>
        b.count - a.count ||
        a.value.localeCompare(b.value, "zh-CN", { numeric: true })
    );
}

async function collectSource({ key, dir }) {
  const categories = new Map();
  const subcategories = new Map();
  const tags = new Map();

  for (const file of await listMarkdownFiles(dir)) {
    const frontmatter = extractFrontmatter(await readFile(file, "utf8"));

    addCount(categories, frontmatter.category);
    addCount(subcategories, frontmatter.subcategory);

    if (Array.isArray(frontmatter.tags)) {
      frontmatter.tags.forEach(tag => addCount(tags, tag));
    }
  }

  return {
    categories: toOptions(categories, recommended[key].categories),
    subcategories: toOptions(subcategories, recommended[key].subcategories),
    tags: toOptions(tags),
  };
}

const taxonomy = Object.fromEntries(
  await Promise.all(
    sources.map(async source => [source.key, await collectSource(source)])
  )
);

const file = `// Generated by scripts/generate-cms-taxonomy.mjs.
// Do not edit by hand. Run \`npm run cms:taxonomy\` after adding many new tags.

export type CmsTaxonomyOption = {
  label: string;
  value: string;
  count: number;
};

export const postCategorySuggestions = ${JSON.stringify(
  taxonomy.posts.categories,
  null,
  2
)} satisfies CmsTaxonomyOption[];

export const postSubcategorySuggestions = ${JSON.stringify(
  taxonomy.posts.subcategories,
  null,
  2
)} satisfies CmsTaxonomyOption[];

export const postTagSuggestions = ${JSON.stringify(
  taxonomy.posts.tags,
  null,
  2
)} satisfies CmsTaxonomyOption[];

export const resourceCategorySuggestions = ${JSON.stringify(
  taxonomy.resources.categories,
  null,
  2
)} satisfies CmsTaxonomyOption[];

export const resourceSubcategorySuggestions = ${JSON.stringify(
  taxonomy.resources.subcategories,
  null,
  2
)} satisfies CmsTaxonomyOption[];

export const resourceTagSuggestions = ${JSON.stringify(
  taxonomy.resources.tags,
  null,
  2
)} satisfies CmsTaxonomyOption[];
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, file);
process.stdout.write(`Generated ${path.relative(root, outputPath)}\n`);
